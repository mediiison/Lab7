def plot_forecast(ax, title, y_true, pred, color,
                  train_vals=None, train_idx=None, test_idx=None):
    if train_vals is not None:
        ax.plot(train_idx, train_vals,
                color='steelblue', alpha=0.4, label='Обучение')
    ax.plot(test_idx if test_idx is not None else range(len(y_true)),
            y_true, color='steelblue', linewidth=2, label='Факт (тест)')
    n = min(len(y_true), len(pred))
    idx = test_idx[:n] if test_idx is not None else range(n)
    ax.plot(idx, pred[:n], color=color,
            linewidth=2, linestyle='--', label='Прогноз')
    ax.set_title(title, fontweight='bold')
    ax.legend(fontsize=8)
    ax.set_ylabel('Пассажиры')


from gplearn.genetic import SymbolicRegressor

MAX_LAG = 12

def make_lag_features(s, lags=12):
    data = {'y': s.values}
    for lag in range(1, lags + 1):
        data[f'lag_{lag}'] = np.roll(s.values, lag)
    df_f = pd.DataFrame(data)
    df_f = df_f.iloc[lags:]
    return df_f['y'].values, df_f.drop(columns='y').values

y_all, X_all = make_lag_features(series, lags=MAX_LAG)
split_idx = TRAIN_SIZE - MAX_LAG

X_train_sr = X_all[:split_idx]
y_train_sr = y_all[:split_idx]
X_test_sr  = X_all[split_idx:]
y_test_sr  = y_all[split_idx:]

sr_model = SymbolicRegressor(
    population_size=500,
    generations=20,
    p_crossover=0.7,
    p_subtree_mutation=0.1,
    p_hoist_mutation=0.05,
    p_point_mutation=0.1,
    max_samples=0.9,
    parsimony_coefficient=0.01,
    random_state=42,
    verbose=1,
    n_jobs=-1
)
sr_model.fit(X_train_sr, y_train_sr)
sr_forecast = sr_model.predict(X_test_sr)

mae_sr  = mean_absolute_error(y_test_sr, sr_forecast)
rmse_sr = np.sqrt(mean_squared_error(y_test_sr, sr_forecast))

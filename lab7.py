print(f"RMSE: {rmse_sr:.2f}")
print(f"MAPE: {mape_sr:.2f}%")
print(f"\nФормула: {sr_model._program}")

# ── Ячейка 8: Подготовка данных для МГУА ──────────────────────
LAGS = 6

scaler = MinMaxScaler()
series_scaled = scaler.fit_transform(
    series.values.reshape(-1, 1)).flatten()

def make_gmdh_features(s, lags=6):
    X = np.array([s[i - lags:i] for i in range(lags, len(s))])
    y = s[lags:]
    return X, y

X_gmdh, y_gmdh = make_gmdh_features(series_scaled, lags=LAGS)
split_gmdh = int(len(X_gmdh) * 0.8)

